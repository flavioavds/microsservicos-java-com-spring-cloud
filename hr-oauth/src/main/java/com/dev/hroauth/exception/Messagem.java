public class Messagem {
	
	private String infoMessage;

	public Messagem(String infoMessage) {
		super();
		this.infoMessage = infoMessage;
	}

	public String getInfoMessage() {
		return infoMessage;
	}

	public void setInfoMessage(String infoMessage) {
		this.infoMessage = infoMessage;
	}

}
